function changeLanguage(lang, label) {
    document.querySelectorAll("[data-i18n]").forEach(element => {
        const key = element.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });
    if (label) {
        document.getElementById("current-lang-lbl").innerText = label;
        toggleDropdown();
    }
}

function toggleDropdown() {
    document.querySelector('.custom-dropdown').classList.toggle('active');
}

document.addEventListener('click', function(e) {
    const dropdown = document.querySelector('.custom-dropdown');
    if (dropdown && !dropdown.contains(e.target) && dropdown.classList.contains('active')) {
        dropdown.classList.remove('active');
    }
});
